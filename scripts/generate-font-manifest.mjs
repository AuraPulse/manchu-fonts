import { mkdir, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const rootDir = process.cwd();
const fontDir = path.join(rootDir, 'manchufonts');
const outputDir = path.join(rootDir, 'src', 'generated');
const outputFile = path.join(outputDir, 'fontManifest.ts');

const labelize = (fileName) =>
  fileName
    .replace(/\.[^.]+$/, '')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();

const slugify = (fileName) =>
  fileName
    .replace(/\.[^.]+$/, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

const files = (await readdir(fontDir))
  .filter((file) => /\.(ttf|otf)$/i.test(file))
  .sort((left, right) => left.localeCompare(right, 'en'));

const imports = files.map((file, index) => {
  const relativePath = `../../manchufonts/${file}`;
  return `const fontAsset${index} = new URL(${JSON.stringify(relativePath)}, import.meta.url).href;`;
});

const entries = files.map((file, index) => {
  const id = slugify(file);
  const label = labelize(file);
  const family = `ManchuPreview${String(index + 1).padStart(2, '0')}`;

  return `  {
    id: ${JSON.stringify(id)},
    label: ${JSON.stringify(label)},
    family: ${JSON.stringify(family)},
    file: fontAsset${index},
    sourceFile: ${JSON.stringify(file)},
  }`;
});

const content = `/* This file is auto-generated by scripts/generate-font-manifest.mjs. */
export type FontOption = {
  id: string;
  label: string;
  family: string;
  file: string;
  sourceFile: string;
};

${imports.join('\n')}

export const FONT_OPTIONS: FontOption[] = [
${entries.join(',\n')}
];
`;

await mkdir(outputDir, { recursive: true });
await writeFile(outputFile, content, 'utf8');

console.log(`Generated font manifest with ${files.length} fonts.`);
